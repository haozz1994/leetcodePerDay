    public boolean hasCycle(ListNode head) {
        if (head == null) {
            return false;
        }
        Set<Integer> set = new HashSet<>();
        while (head.next != null) {
            if (!set.add(head.val)) {
                return true;
            }
            head = head.next;
        }
        return false;
    }
